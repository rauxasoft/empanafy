CREATE TABLE INTERPRETES(

	CODIGO					INTEGER			NOT NULL,
	NOMBRE					VARCHAR(50)		,
	APELLIDOS				VARCHAR(150)	,
	NACIONALIDAD			VARCHAR(100)	,
	FECHA_NACIMIENTO		DATE			,
	FECHA_DEFUNCION			DATE			,
	
	PRIMARY KEY (CODIGO)
);

CREATE TABLE COMPOSITORES(

	CODIGO					INTEGER			NOT NULL,
	NOMBRE					VARCHAR(50)		,
	APELLIDOS				VARCHAR(150)	,
	NACIONALIDAD			VARCHAR(100)	,
	FECHA_NACIMIENTO		DATE			,
	FECHA_DEFUNCION			DATE			,
	
	PRIMARY KEY (CODIGO)
);

CREATE TABLE GENEROS(

	CODIGO					INTEGER       	NOT NULL,
	NAME					VARCHAR(50)		NOT NULL,
	OBSERVACIONES			VARCHAR(100)	,
	
	PRIMARY KEY (CODIGO)
);

CREATE TABLE CANCIONES(

	ISMN					VARCHAR(20)		NOT NULL,
	TITULO					VARCHAR(250)	,
	FECHA_CREACION			DATE			,
	CODIGO_GENERO			INTEGER			,
	CODIGO_COMPOSITOR		INTEGER			,
	CODIGO_INTERPRETE		INTEGER			,
	EXPLICITA				BOOLEAN			,
	INSTRUMENTAL			BOOLEAN			,
	PUNTUACION				INTEGER			,
	DURACION				INTEGER			,
	OBSERVACIONES			TEXT			,
	
	PRIMARY KEY (ISMN),
	FOREIGN KEY (CODIGO_GENERO) REFERENCES GENEROS (CODIGO),
	FOREIGN KEY (CODIGO_COMPOSITOR) REFERENCES COMPOSITORES (CODIGO),
	FOREIGN KEY (CODIGO_INTERPRETE) REFERENCES INTERPRETES (CODIGO)
);
